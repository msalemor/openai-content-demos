function splitIntoParagraphs(text: string): string[] {
  const paragraphs = text.split(/\r?\n\n/);
  return paragraphs;
}

function WordCount(text: string): number {
  const words = text.trim().split(/\s+/);
  return words.length;
}

export function BreakIntoSections(text: string): string[] {
  const result: string[] = [];
  const paragraphs = splitIntoParagraphs(text);
  let test = "";
  let count = 0;
  while (count < paragraphs.length) {
    test += paragraphs[count].trim() + "\n\n";
    if (WordCount(test) > 200) {
      result.push(test);
      test = "";
    }
    count++;
  }
  if (test.trim() != "") {
    result.push(test.trim() + "\n\n");
  }
  return result;
}

function breakIntoBlocks(text: string): string[] {
  const maxWords = 500;
  const paragraphs = text.split(/\r?\n\n/);
  const blocks: string[] = [];

  for (const paragraph of paragraphs) {
    const words = paragraph.split(" ");
    let currentBlock = "";
    let wordsInBlock = 0;

    for (const word of words) {
      if (wordsInBlock >= maxWords) {
        blocks.push(currentBlock.trim());
        currentBlock = "";
        wordsInBlock = 0;
      }
      currentBlock += word + " ";
      wordsInBlock++;

      // Check for the closest paragraph ending
      if (/\.\s*$/.test(currentBlock)) {
        blocks.push(currentBlock.trim());
        currentBlock = "";
        wordsInBlock = 0;
      }
    }

    // Add any remaining text to the last block
    if (currentBlock.trim()) {
      blocks.push(currentBlock.trim());
    }
  }

  return blocks;
}
